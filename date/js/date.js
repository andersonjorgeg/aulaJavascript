var d = new Date();
console.log(d)
console.log(d.getDate())//dia atual
console.log(d.getMonth()+1)//mês atual
console.log(d.getFullYear())//ano
console.log(d.getDay())//dia da semana em posição array
console.log(d.getMinutes())//minutos
console.log(d.getTimezoneOffset())//Obtém a diferença em minutos entre a hora no computador local e a Hora Universal Coordenada
console.log(d.toDateString())//Retorna uma data como um valor de string.

